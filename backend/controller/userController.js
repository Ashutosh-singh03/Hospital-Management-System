export const addNewDoctor = catchAsyncErrors(async (req, res, next) => {
  if (!req.files || Object.keys(req.files).length === 0) {
    return next(new ErrorHandler("Doctor Avatar Required!", 400));
  }
  const { docAvatar } = req.files;
  const allowedFormats = ["image/png", "image/jpeg", "image/webp"];
  if (!allowedFormats.includes(docAvatar.mimetype)) {
    return next(new ErrorHandler("File Format Not Supported!", 400));
  }
  const {
    firstName,
    lastName,
    email,
    phone,
    aadhar,
    dob,
    gender,
    password,
    doctorDepartment,
  } = req.body;
  
  console.log("Received doctor data:", {
    firstName,
    lastName,
    email,
    phone,
    aadhar,
    dob,
    gender,
    doctorDepartment
  });

  if (
    !firstName ||
    !lastName ||
    !email ||
    !phone ||
    !aadhar ||
    !dob ||
    !gender ||
    !password ||
    !doctorDepartment ||
    !docAvatar
  ) {
    return next(new ErrorHandler("Please Fill Full Form!", 400));
  }
  const isRegistered = await User.findOne({ email });
  if (isRegistered) {
    return next(
      new ErrorHandler("Doctor With This Email Already Exists!", 400)
    );
  }
  const cloudinaryResponse = await cloudinary.uploader.upload(
    docAvatar.tempFilePath
  );
  if (!cloudinaryResponse || cloudinaryResponse.error) {
    console.error(
      "Cloudinary Error:",
      cloudinaryResponse.error || "Unknown Cloudinary error"
    );
    return next(
      new ErrorHandler("Failed To Upload Doctor Avatar To Cloudinary", 500)
    );
  }

  
  console.log("Creating new doctor in database...");
  const doctor = await User.create({
    firstName,
    lastName,
    email,
    phone,
    aadhar,
    dob,
    gender,
    password,
    role: "Doctor",
    doctorDepartment,
    docAvatar: {
      public_id: cloudinaryResponse.public_id,
      url: cloudinaryResponse.secure_url,
    },
    
  });
  console.log("Doctor created successfully:", doctor);

  res.status(200).json({
    success: true,
    message: "New Doctor Registered",
    doctor,
  });
});

export const getAllDoctors = catchAsyncErrors(async (req, res, next) => {
  console.log("Fetching doctors from database...");
  try {
    // First, let's check if there are any users in the database
    const totalUsers = await User.countDocuments();
    console.log("Total users in database:", totalUsers);
    
    // Then, let's check how many users have role "Doctor"
    const doctorCount = await User.countDocuments({ role: "Doctor" });
    console.log("Number of users with role 'Doctor':", doctorCount);
    
    // Now let's get all doctors
    const doctors = await User.find({ role: "Doctor" });
    console.log("Found doctors:", doctors);
    console.log("Number of doctors found:", doctors.length);
    
    // Let's also check the first doctor's details if any exist
    if (doctors.length > 0) {
      console.log("First doctor details:", {
        _id: doctors[0]._id,
        firstName: doctors[0].firstName,
        lastName: doctors[0].lastName,
        role: doctors[0].role,
        doctorDepartment: doctors[0].doctorDepartment
      });
    }
    
    res.status(200).json({
      success: true,
      doctors,
    });
  } catch (error) {
    console.error("Error in getAllDoctors:", error);
    next(error);
  }
});

